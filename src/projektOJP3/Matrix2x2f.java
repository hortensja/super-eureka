package projektOJP3;


public class Matrix2x2f {

	/**
	 *a11 a12
	 *a21 a22
	 */
	public final float a11;
	public final float a12;
	public final float a21;
	public final float a22;


	public Matrix2x2f(float a11, float a12, float a21, float a22){
		this.a11 = a11;
		this.a12 = a12;
		this.a21 = a21;
		this.a22 = a22;
	}

}
